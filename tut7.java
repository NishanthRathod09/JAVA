class Relational_Operator {

  public static void main(String[] args) {
    int a = 55, b = 89;
    System.out.println("a>b:" + (a > b));
    System.out.println("a<b:" + (a < b));
    System.out.println("a==b:" + (a == b));
    System.out.println("a!=b:" + (a != b));
    System.out.println("a>=b:" + (a >= b));
    System.out.println("a<=b:" + (a <= b));
  }
}
